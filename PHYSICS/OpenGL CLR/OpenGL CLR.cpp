#include "pch.h"
#include "OpenGL CLR.h"

namespace OpenGL_CLR
{
	double OpenGL_Lib::Sum(double a, double b)
	{
		return a + b;
	}

	double OpenGL_Lib::Sub(double a, double b)
	{
		return a - b;
	}

	double OpenGL_Lib::Mul(double a, double b)
	{
		return a * b;
	}

	double OpenGL_Lib::Div(double a, double b)
	{
		return a / b;
	}
}